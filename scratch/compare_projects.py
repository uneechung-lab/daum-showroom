import re

def extract_projects(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Extract projects from grid
    pattern = r'<div class="project-info"><h3><span class="company-name">([^<]+)</span>\s*([^<]+)</h3></div>'
    matches = re.findall(pattern, content)
    
    # Extract projects from carousel
    carousel_pattern = r'<h3><span class="company-name">([^<]+)</span>\s*([^<]+)</h3>'
    carousel_matches = re.findall(carousel_pattern, content)
    
    return [f"{m[0]} {m[1]}".strip() for m in matches + carousel_matches]

